SELECT

    '2-Mov.del Periodo' AS TIPO,

    V_TRCONTABLE_.NUMERODOCUMENTO,

    V_TRCONTABLE_.NOMBRE,

    SUM(V_VALOR_.IMPORTE) AS DEBE,

    SUM(V_VALOR_1.IMPORTE) AS HABER,

    0 AS SALDO,

    V_TRCONTABLE_.FECHAACTUAL,

    SUBSTRING(V_TRCONTABLE_.FECHAACTUAL, 1, 4) AS ANIO,

    SUBSTRING(V_TRCONTABLE_.FECHAACTUAL, 5, 2) AS MES,

    SUBSTRING(V_TRCONTABLE_.FECHAACTUAL, 7, 2) AS DIA,

    V_TRCONTABLE_.FECHAAPLICACION,

    COALESCE(V_EZI_ASIENTOS_DETALLES.DETALLE, V_TRCONTABLE_.DETALLE) AS DETALLE,

    V_TRCONTABLE_.VALORTOTAL AS TOTAL,

    -SUM(V_VALOR_1.IMPORTE) AS RESTAHABER,

    V_ITEMCONTABLE_.DESCRIPCION,

    V_EZI_CUENTAS.CODIGO,

    CASE

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '89C235FC-3F01-11D5-86AD-0080AD403F5F' THEN 'DTA'

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = 'EA17FA63-D4C8-4C97-951C-D74EA44B8917' THEN 'BLC'

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = 'E6D4EF31-1D97-484D-838F-C16F7DB2FD31' THEN 'AALC'

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '6A240BE0-160C-4F6A-8018-29DCCA57032E' THEN 'SAS'

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '3E81DE7D-7813-492E-B12C-1ECD28401637' THEN '9-INGENIO'

        ELSE ''

    END AS un,

    V_EZI_CUENTAS.CRubro,

    V_EZI_CUENTAS.NRubro,

    V_EZI_CUENTAS.CSubrubro1,

    V_EZI_CUENTAS.NSubrubro1,

    V_EZI_CUENTAS.CSubrubro2,

    V_EZI_CUENTAS.NSubrubro2,

    V_EZI_CUENTAS.CSubrubro3,

    V_EZI_CUENTAS.NSubrubro3,

    V_TRCONTABLE_.NOMBREDESTINATARIO AS PROVEEDOR,

    V_CENTROCOSTOS_.NOMBRE AS CC,

    V_EZI_ASIENTOS_DETALLES.TC

FROM

    V_TRCONTABLE_

    INNER JOIN V_ITEMCONTABLE_ ON V_TRCONTABLE_.ITEMSTRANSACCION_ID = V_ITEMCONTABLE_.BO_PLACE_ID

    INNER JOIN V_VALOR_ ON V_ITEMCONTABLE_.DEBE_ID = V_VALOR_.ID

    INNER JOIN V_VALOR_ AS V_VALOR_1 ON V_ITEMCONTABLE_.HABER_ID = V_VALOR_1.ID

    INNER JOIN V_EZI_CUENTAS ON V_ITEMCONTABLE_.REFERENCIA_ID = V_EZI_CUENTAS.ID

    LEFT OUTER JOIN V_EZI_ASIENTOS_DETALLES ON V_TRCONTABLE_.ID = V_EZI_ASIENTOS_DETALLES.ID

    LEFT OUTER JOIN V_CENTROCOSTOS_ ON V_ITEMCONTABLE_.CENTROCOSTOS_ID = V_CENTROCOSTOS_.ID

WHERE

    SUBSTRING(V_TRCONTABLE_.FECHAAPLICACION, 1, 8) >= '20260501'

    AND V_EZI_CUENTAS.CODIGO BETWEEN '0' AND '9'

    AND V_TRCONTABLE_.ESTADO = 'C'

GROUP BY

    V_TRCONTABLE_.NUMERODOCUMENTO,

    V_TRCONTABLE_.NOMBRE,

    V_TRCONTABLE_.FECHAACTUAL,

    SUBSTRING(V_TRCONTABLE_.FECHAACTUAL, 1, 4),

    SUBSTRING(V_TRCONTABLE_.FECHAACTUAL, 5, 2),

    SUBSTRING(V_TRCONTABLE_.FECHAACTUAL, 7, 2),

    V_TRCONTABLE_.FECHAAPLICACION,

    V_TRCONTABLE_.VALORTOTAL,

    V_ITEMCONTABLE_.DESCRIPCION,

    V_EZI_CUENTAS.CODIGO,

    COALESCE(V_EZI_ASIENTOS_DETALLES.DETALLE, V_TRCONTABLE_.DETALLE),

    CASE

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '89C235FC-3F01-11D5-86AD-0080AD403F5F' THEN 'DTA'

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = 'EA17FA63-D4C8-4C97-951C-D74EA44B8917' THEN 'BLC'

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = 'E6D4EF31-1D97-484D-838F-C16F7DB2FD31' THEN 'AALC'

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '6A240BE0-160C-4F6A-8018-29DCCA57032E' THEN 'SAS'

        WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '3E81DE7D-7813-492E-B12C-1ECD28401637' THEN '9-INGENIO'

        ELSE ''

    END,

    V_EZI_CUENTAS.CRubro,

    V_EZI_CUENTAS.NRubro,

    V_EZI_CUENTAS.CSubrubro1,

    V_EZI_CUENTAS.NSubrubro1,

    V_EZI_CUENTAS.CSubrubro2,

    V_EZI_CUENTAS.NSubrubro2,

    V_EZI_CUENTAS.CSubrubro3,

    V_EZI_CUENTAS.NSubrubro3,

    V_TRCONTABLE_.NOMBREDESTINATARIO,

    V_CENTROCOSTOS_.NOMBRE,

    V_TRCONTABLE_.ESTADO,

    V_EZI_ASIENTOS_DETALLES.TC;				
					
					select CASE
								WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '89C235FC-3F01-11D5-86AD-0080AD403F5F' THEN 'DTA'        
								WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = 'EA17FA63-D4C8-4C97-951C-D74EA44B8917' THEN 'BLC'        
								WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = 'E6D4EF31-1D97-484D-838F-C16F7DB2FD31' THEN 'AALC'        
								WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '6A240BE0-160C-4F6A-8018-29DCCA57032E' THEN 'SAS'        
								WHEN V_TRCONTABLE_.UNIDADOPERATIVA_ID = '3E81DE7D-7813-492E-B12C-1ECD28401637' THEN '9-INGENIO'        
								ELSE '' END AS unidad_negocio
					from V_TRCONTABLE_
					where	UNIDADOPERATIVA_ID = '89C235FC-3F01-11D5-86AD-0080AD403F5F' OR 
							UNIDADOPERATIVA_ID = 'EA17FA63-D4C8-4C97-951C-D74EA44B8917' OR
							UNIDADOPERATIVA_ID = 'E6D4EF31-1D97-484D-838F-C16F7DB2FD31' OR
							UNIDADOPERATIVA_ID = '6A240BE0-160C-4F6A-8018-29DCCA57032E' OR
							UNIDADOPERATIVA_ID = '3E81DE7D-7813-492E-B12C-1ECD28401637'							
					group by UNIDADOPERATIVA_ID
					ORDER BY UNIDADOPERATIVA_ID ASC;